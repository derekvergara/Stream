    private void guardarInf(File dct){
        ArrayList<String> datos= new ArrayList();
        
        try {
            FileReader leer= new FileReader(dct);
            BufferedReader inicio= new BufferedReader(leer);
            String linea=inicio.readLine();
            while(linea!=null){
               datos.add(linea); 
                linea=inicio.readLine();
 
            }
            crearPersonas(datos);
        } catch (IOException e) {
            Logger.getLogger(CargaStream.class.getName()).log(Level.SEVERE, null, e);
            
        }
        
        
    }
    private void crearPersonas(ArrayList<String> lista) throws IOException{
        ArrayList<Persona> datos1=new ArrayList();
      
        lista.stream().forEach(li -> {
            datos1.add(new Persona(li.split(" ")[0],li.split(" ")[1],li.split(" ")[2]));
        });
        
        StreamPantalla SP=new StreamPantalla();
        SP.setVisible(true);
        SP.setLocationRelativeTo(null);
        SP.datos(datos1);
        
    }
